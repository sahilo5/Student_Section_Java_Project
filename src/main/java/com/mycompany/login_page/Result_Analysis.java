    private void enter_raActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enter_raActionPerformed
                // Strings that will act as an table name when concatinated together
        
        boolean checkFlag = false;
        String department = (String) department_name.getSelectedItem();
        String yr = (String) year.getSelectedItem();
        String sem = (String) semester.getSelectedItem();
        String exm = (String) exam_name.getSelectedItem();
        String acadYear = (String) academic_year.getSelectedItem();
        
        
        if(department=="--Select--" || yr=="--Select--"  || sem=="--Select--"  || exm=="--Select--" || acadYear=="--Select--" ){
            JOptionPane.showMessageDialog(this,"Please check if fields are selected or not !"); 
        }else{
            
        //Formation of table name 
        exm = exm.replace(" ","");
        acadYear = acadYear.replace("-","_");
        tableName = department+"_"+yr+"_"+sem+"_"+exm+"_"+acadYear;
        
//        check whether database contains the table or not
    
        try {
          Connection connection = null;
        Statement statement = null;
        ResultSet resultSet = null;

           connection = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;instance=LAPTOP-AVH935TU\\SQLEXPRESS;databaseName=StudentSection;user=sa;password=12345;encrypt=true;trustServerCertificate=true;");
            
            statement = connection.createStatement();
            
             String query = "SELECT 1 FROM sys.tables WHERE name = '" + tableName + "'";
            resultSet = statement.executeQuery(query);
            
           if(resultSet.next()){
                checkFlag = true;
                
            }else{
                checkFlag = false;
            }

        }catch (Exception e) {
                e.printStackTrace();
        } 
        
        
        
        //Validate actions
        if(action.getSelectedItem()=="Add"){
            if(checkFlag==true){
                  int confirmationResult = JOptionPane.showConfirmDialog(null, "Result Analysis Data of "+ tableName + " is already present. Do you want to proceed to update?");
                  if (confirmationResult == JOptionPane.YES_OPTION) {
                      this.hide();
//                      JOptionPane.showMessageDialog(this,"To next Page"); 
                  AddMarks addM=new AddMarks();
                  addM.setVisible(true);
                  addM.setExtendedState(JFrame.MAXIMIZED_BOTH);
                  
                  } 
            }else{ 
                this.hide();
                New_Result nr = new New_Result(tableName);
                nr.setVisible(true);
            }
            
        }else if(action.getSelectedItem()=="Update"){
            AddMarks addM=new AddMarks();
                  addM.setVisible(true);
                  addM.setExtendedState(JFrame.MAXIMIZED_BOTH); 
        }else if(action.getSelectedItem()=="Print"){
//            JOptionPane.showMessageDialog(this,"Printed"); 
                PrintData pd=new PrintData();
                pd.setVisible(true);
                pd.setExtendedState(JFrame.MAXIMIZED_BOTH);
                this.hide();
        
        }else{
            JOptionPane.showMessageDialog(this,"Select atleast one action to do !"); 
        }
        }
    }//GEN-LAST:event_enter_raActionPerformed

